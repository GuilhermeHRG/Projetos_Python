"""
Faça um Programa que converta metros para centímetros.
"""
# ENTRADA DE DADOS DO USUÁRIO
mt=float(input("Digite um valor em Metros: "))

# AQUI A VARIÁVEL CT (CENTIMETROS) RECEBE O CÁLCULO DE CONVERSÃO METROS / CENTÍMETROS
ct=mt*100

# SAÍDA DE DADOS
print(f'\nO valor de {mt} metros em cm fica :{ct} cm.')
